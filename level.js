function parseLevel(levelMap) {
    const blocks = [];
    const enemies = [];
    let playerStart = { x: 50, y: 50 };

    for (let row = 0; row < levelMap.length; row++) {
        for (let col = 0; col < levelMap[row].length; col++) {
            const char = levelMap[row][col];
            const x = col * TILE_SIZE;
            const y = row * TILE_SIZE;

            if (char === 'G') {
                blocks.push(new Block(x, y, TILE_SIZE, TILE_SIZE, 'ground'));
            } else if (char === 'B') {
                blocks.push(new Block(x, y, TILE_SIZE, TILE_SIZE, 'brick'));
            } else if (char === '?') {
                blocks.push(new Block(x, y, TILE_SIZE, TILE_SIZE, 'question'));
            } else if (char === 'M') {
                blocks.push(new Block(x, y, TILE_SIZE, TILE_SIZE, 'question_mushroom'));
            } else if (char === 'E') {
                enemies.push(new Enemy(x, y, TILE_SIZE, TILE_SIZE)); // Goomba
            } else if (char === 'K') {
                enemies.push(new Koopa(x, y, TILE_SIZE, TILE_SIZE + 10)); // Koopas are slightly taller
            } else if (char === 'F') {
                blocks.push(new Flagpole(x, y - TILE_SIZE * 5, TILE_SIZE, TILE_SIZE * 6));
            } else if (char === 'P') {
                playerStart = { x, y };
            }
        }
    }
    return { blocks, enemies, playerStart };
}
